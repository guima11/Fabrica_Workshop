# Utilize estrutura de repeticao for para fazer a tabuada de adição de 0 a 10 de um numero digitado no terminal.

##------------------------------------------------------------------------------------------------

# Entrada de Dados
Num = float(input("Insira um número qualquer para ser somado: "))
# Estrutura de Loop para produção de Print dos valores
for i in range (0,10):
    print (Num+i)
