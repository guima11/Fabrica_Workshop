# Crie um programa que leia dois números e que exiba `True` se o primeiro número for maior que o segundo e `False` se o primeiro numero for menor ou igual ao segundo numero

##------------------------------------------------------------------------------------------------

# Entrada de Dados

Num1 = float(input("Insira o primeiro número para a operação: "))

Num2 = float(input("Insira o segundo número para a operação: "))

# Cálculo de dados e saída

if Num1 > Num2:
    print("True")

else:
    print("False")
